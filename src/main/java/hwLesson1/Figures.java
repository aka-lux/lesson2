package hwLesson1;

public class Figures {

    /*
     * Площадь равностороннего треугольника равна Sтреуг= √3/4*a^2
     */
    public static Double triangle(int a){
        return Math.sqrt(3)/4*a*a;
    }

    /*
     * Площадь квадрата S=a^2
     */
    public static long square(int a){
        return (long) a*a;
    }

}
